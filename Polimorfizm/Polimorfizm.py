# len() - zwraca długość czegoś

słowo = "Tester Automatyzujący"
print(f"Nasze słowo ma {len(słowo)} znaków")

owoce = ["Jabłko", "Banan", "Pomarańcza"]
print(f"Lista owoce ma {len(owoce)} elementów")

samochód = {
    "Marka" : "Ford",
    "Model" : "Mustang",
    "Rocznik" : 1969
}
print(f"w słowniku samochód znajdują się {len(samochód)} elementy")